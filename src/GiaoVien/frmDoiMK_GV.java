    private void btnxacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxacNhanActionPerformed
        // TODO add your handling code here:
        String mkCuNhap = mkCuTxt.getText();
        String mkMoi = mkMoiTxt.getText();
        String mkMoiXn = xnMkNoiTxt.getText();
        

        
        try {
            if("".equals(mkCuNhap)){           
                GUI.Main.thongBao("Mật khẩu không được bỏ trống", "Thông Báo", 1);
                mkCuTxt.requestFocus();
            }
            else if("".equals(mkMoi)){           
                GUI.Main.thongBao("Mật khẩu không được bỏ trống", "Thông Báo", 1);
                mkMoiTxt.requestFocus();
            }
            else if("".equals(mkMoiXn)){           
                GUI.Main.thongBao("Mật khẩu không được bỏ trống", "Thông Báo", 1);
                xnMkNoiTxt.requestFocus();
            }
            else if(!mkMoi.equals(mkMoiXn)){           
                GUI.Main.thongBao("Mật khẩu xác nhận không chính xác", "Thông Báo", 1);
                mkMoiTxt.requestFocus();
            }
            else if(mkMoi.equals(mkCu)){           
                GUI.Main.thongBao("Mật khẩu này đang được dùng !!!", "Thông Báo", 1);
                mkCuTxt.requestFocus();
            }
            else if(!mkCuNhap.equals(mkCu)){           
                GUI.Main.thongBao("Mật khẩu cũ không chính xác!!!", "Thông Báo", 1);
                mkCuTxt.requestFocus();
            }
            else {
                if (capNhapMK(mkMoi)) {
                    mkCuTxt.setText("");
                    mkMoiTxt.setText("");
                    xnMkNoiTxt.setText("");
                }
            }
        } catch (Exception e) {
            GUI.Main.thongBao("Lỗi trong quá trình đổi mật khẩu", "Thông Báo", 1);
        }
    }//GEN-LAST:event_btnxacNhanActionPerformed

